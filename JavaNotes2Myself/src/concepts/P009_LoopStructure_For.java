package concepts;

/*
 * Description: For Loop Structure in Java
 * Note
 * 1. The loop consists of 3 parts: Initialisation, condition checking, operation on an expression
 * 2. All three parts are optional and can remain empty.
 */

public class P009_LoopStructure_For {

    public static void main(String args[]) {

        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}