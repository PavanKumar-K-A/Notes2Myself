package concepts;

/*
 * Description: While Loop Structure in Java
 * Note
 * 1. The while loop evaluates till the condition remains true.
 */

public class P010_LoopStructure_While {

    public static void main(String args[]) {

        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }
    }
}
