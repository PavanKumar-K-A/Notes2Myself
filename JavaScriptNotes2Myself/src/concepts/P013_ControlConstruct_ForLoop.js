// Description: For Loop Construct in JavaScript

// Note
// 1. Statement 1 (Initialization): The statement var i = 0; is executed before the loop (ie the code block)
//    starts.
// 2. Statement 2 (Condition): The statement var i < 10; defines the condition for running the loop (ie the
//    code block).
// 3. Statement 3 (Increment): The statement i++; is executed each time after the loop (ie the code block)
//    execution.
// 4, All 3 statements above are optional.
// 5. If the condition (Statement 2) is omitted, then a condition of true is assumed.
for ( var i = 0; i < 10; i++) {
    console.log("i = ", i);
}