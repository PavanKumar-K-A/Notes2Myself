# Description: env - run a program in a modified environment

# Notes
# 1. The printenv commmand is an alternative for env for outputing environment variables.

# Common Examples

# Examples with details
env         # Print a list of all the current environment variables.
env -0      # Use -0 or -null switch to output line with a 0 (null) byte rather than a newline.

