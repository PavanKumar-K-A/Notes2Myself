# Description: Print Variables Using Format Specifications

variable_string = "Hello World"
variable_integer = 24
variable_float = 2.34

print ("Printing a String\t:", variable_string)
print ("Printing a String\t: %s" % variable_string)
print ("Printing a Number\t: %d" % variable_integer)
print ("Printing a Decimal\t: %f" % variable_float)
print ("Multiple Formats\t: %s, %d, %f" % (variable_string, variable_integer, variable_float))
print ("Multiple Formats\t: %s, %d, %f" % (variable_string, variable_integer, variable_float))
